<?php
class matematica {
    public static function sumar($num1,$num2){
        $suma=$num1+$num2;
        return $suma;
    }
// esto realiza una suma de variables de num1 + num 2

    public static function restar($num1,$num2){
        $resta=$num1-$num2;
        return $resta;
    }
// esto realiza una resta de variables de num1 - num 2
    public static function multiplicar($num1,$num2){
        $multi=$num1*$num2;
        return $multi;
    }
// esto realiza una multi de variables de num1 * num2
    public static function dividir($num1,$num2){
        $divi=$num1/$num2;
        return $divi;
    }
}// esto realiza una divi de variables de num1 / num2
?>